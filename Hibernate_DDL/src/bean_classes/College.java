package bean_classes;

public class College 
{
	private int cid;
	private String cname;
	private double cstudents;
	public int getCid() {
		return cid;
	}
	public void setCid(int cid) {
		this.cid = cid;
	}
	public String getCname() {
		return cname;
	}
	public void setCname(String cname) {
		this.cname = cname;
	}
	public double getCstudents() {
		return cstudents;
	}
	public void setCstudents(double cstudents) {
		this.cstudents = cstudents;
	}
}