package bean_classes;

public class School 
{
	private int sid;
	private String sname;
	private double sstudents;
	public int getSid() {
		return sid;
	}
	public void setSid(int sid) {
		this.sid = sid;
	}
	public String getSname() {
		return sname;
	}
	public void setSname(String sname) {
		this.sname = sname;
	}
	public double getSstudents() {
		return sstudents;
	}
	public void setSstudents(double sstudents) {
		this.sstudents = sstudents;
	}
}
