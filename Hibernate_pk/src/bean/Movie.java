package bean;

public class Movie {
private int mid;
private String mname;
private int mticket;
private int seatNos;
public int getMid() {
	return mid;
}
public void setMid(int mid) {
	this.mid = mid;
}
public String getMname() {
	return mname;
}
public void setMname(String mname) {
	this.mname = mname;
}
public int getMticket() {
	return mticket;
}
public void setMticket(int mticket) {
	this.mticket = mticket;
}
public int getSeatNos() {
	return seatNos;
}
public void setSeatNos(int seatNos) {
	this.seatNos = seatNos;
}
}
